int main(void)
{
   #if defined(__PIC24FJ64GB004__) || defined(PIC24FJ256DA210_DEV_BOARD)
	//On the PIC24FJ64GB004 Family of USB microcontrollers, the PLL will not power up and be enabled
	//by default, even if a PLL enabled oscillator configuration is selected (such as HS+PLL).
	//This allows the device to power up at a lower initial operating frequency, which can be
	//advantageous when powered from a source which is not gauranteed to be adequate for 32MHz
	//operation.  On these devices, user firmware needs to manually set the CLKDIV<PLLEN> bit to
	//power up the PLL.
    {
        unsigned int pll_startup_counter = 600;
        CLKDIVbits.PLLEN = 1;
        while(pll_startup_counter--);
    }
    //Device switches over automatically to PLL output after PLL is locked and ready.

    #endif

    #if defined(__PIC32MX__)
        {
            int  value;
    
            value = SYSTEMConfigWaitStatesAndPB( GetSystemClock() );
    
            // Enable the cache for the best performance
            CheKseg0CacheOn();
    
            INTEnableSystemMultiVectoredInt();
    
            value = OSCCON;
            while (!(value & 0x00000020))
            {
                value = OSCCON;    // Wait for PLL lock to stabilize
            }
        }
            
        DDPCONbits.JTAGEN = 0;
    #endif

    mInitAllSwitches();

    while(1)
    {
        switch(device_mode)
        {
            case MODE_HOST:
                //Host mode          
                deviceAttached = FALSE;
            
                //Initialize the stack
                USBInitialize(0);
            
                while(device_mode == MODE_HOST)
                {
                    //USB stack process function
                    USBTasks();
            
                    //if thumbdrive is plugged in
                    if(USBHostMSDSCSIMediaDetect())
                    {
                        deviceAttached = TRUE;
            
                        //now a device is attached
                        //See if the device is attached and in the right format
                        if(FSInit())
                        {
                            myFile = FSfopen("test.txt","w");
                            FSfwrite("This is a test",1,14,myFile);
                            
                            FSfclose(myFile);
                            while(deviceAttached == TRUE)
                            {
                                USBTasks();
                            }
                            mLED_2_Off();
                            device_mode = MODE_UNKNOWN;
                            USBMaskInterrupts();
                        }
                    }
                }
                break;


            case MODE_DEVICE:
           
                InitializeSystem();

                #if defined(USB_INTERRUPT)
                    USBDeviceAttach();
                #endif
            
                while(device_mode == MODE_DEVICE)
                {
                    #if defined(USB_POLLING)
            		// Check bus status and service USB interrupts.
                    USBDeviceTasks(); // Interrupt or polling method.  If using polling, must call
                    				  // this function periodically.  This function will take care
                    				  // of processing and responding to SETUP transactions 
                    				  // (such as during the enumeration process when you first
                    				  // plug in).  USB hosts require that USB devices should accept
                    				  // and process SETUP packets in a timely fashion.  Therefore,
                    				  // when using polling, this function should be called 
                    				  // regularly (such as once every 1.8ms or faster** [see 
                    				  // inline code comments in usb_device.c for explanation when
                    				  // "or faster" applies])  In most cases, the USBDeviceTasks() 
                    				  // function does not take very long to execute (ex: <100 
                    				  // instruction cycles) before it returns.
                    #endif
                				  
            
            		// Application-specific tasks.
            		// Application related code may be added here, or in the ProcessIO() function.
                    ProcessIO();

                    if(U1OTGSTATbits.SESVD == 0)
                    {
                        mLED_1_Off();
                        mLED_2_Off();
                        mLED_3_Off();
                        mLED_4_Off();
                        USBSoftDetach();
                        device_mode = MODE_UNKNOWN;
                    }        
                }//end while

                break;
            default:
                if(sw2 == 0)
                {
                    mInitAllLEDs();
                    mLED_2_On();
                    device_mode = MODE_HOST;
                }  
                else if(sw3 == 0)
                {
                    mInitAllLEDs();
                    mLED_2_On();
                    device_mode = MODE_DEVICE;
                }
                else 
                break;
        }
    }
    return 0;
}//end main


/********************************************************************
 * Function:        static void InitializeSystem(void)
 *
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Side Effects:    None
 *
 * Overview:        InitializeSystem is a centralize initialization
 *                  routine. All required USB initialization routines
 *                  are called from here.
 *
 *                  User application initialization routine should
 *                  also be called from here.                  
 *
 * Note:            None
 *******************************************************************/
static void InitializeSystem(void)
{
    #if defined(__C30__) || defined __XC16__
    	#if defined(__PIC24FJ256DA210__) || defined(__PIC24FJ256GB210__)
    		ANSA = 0x0000;
    		ANSB = 0x0000;
    		ANSC = 0x0000;
    		ANSD = 0x0000;
    		ANSE = 0x0000;
    		ANSF = 0x0000;
    		ANSG = 0x0000;
        #else
        	AD1PCFGL = 0xFFFF;
        #endif  
    #elif defined(__C32__)
        AD1PCFG = 0xFFFF;
    #endif
    
    #if defined(__32MX460F512L__)|| defined(__32MX795F512L__)
    // Configure the PIC32 core for the best performance
    // at the operating frequency. The operating frequency is already set to 
    // 60MHz through Device Config Registers
    SYSTEMConfigPerformance(60000000);
	#endif
    
//	The USB specifications require that USB peripheral devices must never source
//	current onto the Vbus pin.  Additionally, USB peripherals should not source
//	current on D+ or D- when the host/hub is not actively powering the Vbus line.
//	When designing a self powered (as opposed to bus powered) USB peripheral
//	device, the firmware should make sure not to turn on the USB module and D+
//	or D- pull up resistor unless Vbus is actively powered.  Therefore, the
//	firmware needs some means to detect when Vbus is being powered by the host.
//	A 5V tolerant I/O pin can be connected to Vbus (through a resistor), and
// 	can be used to detect when Vbus is high (host actively powering), or low
//	(host is shut down or otherwise not supplying power).  The USB firmware
// 	can then periodically poll this I/O pin to know when it is okay to turn on
//	the USB module/D+/D- pull up resistor.  When designing a purely bus powered
//	peripheral device, it is not possible to source current on D+ or D- when the
//	host is not actively providing power on Vbus. Therefore, implementing this
//	bus sense feature is optional.  This firmware can be made to use this bus
//	sense feature by making sure "USE_USB_BUS_SENSE_IO" has been defined in the
//	HardwareProfile.h file.    
    #if defined(USE_USB_BUS_SENSE_IO)
    tris_usb_bus_sense = INPUT_PIN; // See HardwareProfile.h
    #endif
    
//	If the host PC sends a GetStatus (device) request, the firmware must respond
//	and let the host know if the USB peripheral device is currently bus powered
//	or self powered.  See chapter 9 in the official USB specifications for details
//	regarding this request.  If the peripheral device is capable of being both
//	self and bus powered, it should not return a hard coded value for this request.
//	Instead, firmware should check if it is currently self or bus powered, and
//	respond accordingly.  If the hardware has been configured like demonstrated
//	on the PICDEM FS USB Demo Board, an I/O pin can be polled to determine the
//	currently selected power source.  On the PICDEM FS USB Demo Board, "RA2" 
//	is used for	this purpose.  If using this feature, make sure "USE_SELF_POWER_SENSE_IO"
//	has been defined in HardwareProfile.h, and that an appropriate I/O pin has been mapped
//	to it in HardwareProfile.h.
    #if defined(USE_SELF_POWER_SENSE_IO)
    tris_self_power = INPUT_PIN;	// See HardwareProfile.h
    #endif

    UserInit();
    
    USBDeviceInit();	//usb_device.c.  Initializes USB module SFRs and firmware
    					//variables to known states.
}//end InitializeSystem



